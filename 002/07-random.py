# Numeri casuali

# Per utilizzare certe funzionalità di Python è necessario
# importare moduli esterni (chiamati anche librerie).
#
# Ad esempio, il comando

import random

# importa il modulo "random", necessario per generare numeri
# pseudocasuali
#
# Il comando

a = random.randint(0,100)

# genera un numero pseudocasuale compreso tra 0 e 100 e lo
# assegna alla variabile a

print(a)